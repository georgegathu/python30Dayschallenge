# Built In Functions:
"""
1. all() & any():
    -all() function: takes in an iterable like a list or tuple and returns True if all the iterables of the elements are true, else returns false.
    -any() function: if any of the itertion is true, it will print True
"""
#Example of all()
boolean_list = [True , True]
print(all(boolean_list)) # Prints True in both all() & any()

"""
2.input(): Asks users for input data
"""
 #Example of input():
name = input("What is your name? ")
print(name)

"""
3. pow(): returns the value of x to the power of y (xy)
"""
#Example:
x = pow(44,2)
print(x)
"""
abs(): returns the absolute value of the specified number
"""
#Example:
x = abs(-22)
print(x)
